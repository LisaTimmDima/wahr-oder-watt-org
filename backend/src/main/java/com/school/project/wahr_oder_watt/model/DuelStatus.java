/**
 * Enum repräsentiert den Status eines Duells.
 * Es gibt drei Stati:
 * WAITING: Das Duell ist noch nicht gestartet
 * RUNNING: Das Duell läuft gerade
 * FINISHED: Das Duell ist beendet
 */
public enum DuelStatus {
  WAITING, RUNNING, FINISHED
}
